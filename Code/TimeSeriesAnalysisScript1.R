#LINEAR DYNAMIC MODELING ARIMA and ARIMAX
mod_arima <- auto.arima(ts_data$SP500_Ret)

mod_arimax_cont <- auto.arima(ts_data$SP500_Ret, 
                              xreg = as.matrix(ts_data[, c("Inflation", "VIX")]))

mod_arimax_lag <- auto.arima(ts_data$SP500_Ret, 
                             xreg = as.matrix(ts_data[, c("Inf_Lag", "VIX_Lag")]))

model_comparison <- data.frame(
  Model = c("ARIMA (No Inputs)", "ARIMAX (Contemporaneous)", "ARIMAX (Lagged)"),
  AIC = c(AIC(mod_arima), AIC(mod_arimax_cont), AIC(mod_arimax_lag)),
  BIC = c(BIC(mod_arima), BIC(mod_arimax_cont), BIC(mod_arimax_lag))
)

cat("\nARIMAX Model Selection Comparison\n")
print(model_comparison[order(model_comparison$AIC), ])

# Diagnostics for ARIMAX
cat("\nResidual Diagnostics ARIMAX Contemporaneous\n")
checkresiduals(mod_arimax_cont)



#VAR MODEL & IMPULSE RESPONSE FUNCTIONS
var_data <- data.frame(
  Inflation = as.numeric(ts_data$Inflation),
  VIX = as.numeric(ts_data$VIX),
  SP500_Ret = as.numeric(ts_data$SP500_Ret)
)

# Select the optimal number of lags (p) for the VAR model
lag_selection <- VARselect(var_data, lag.max = 8, type = "const")
cat("\nVAR Lag Selection (Optimal Lags)\n")
print(lag_selection$selection)

# We will use the lag selected by the AIC criterion
optimal_p <- lag_selection$selection["AIC(n)"]

# 6b. Fit the VAR Model
var_model <- VAR(var_data, p = optimal_p, type = "const")

cat("\nVAR Model Summary\n")
summary(var_model)

#Impulse Response Functions (IRF)
# How does a shock to Inflation affect the VIX?
irf_inf_to_vix <- irf(var_model, impulse = "Inflation", response = "VIX", 
                      n.ahead = 12, boot = TRUE)

# How does a shock to Inflation affect the S&P 500?
irf_inf_to_sp500 <- irf(var_model, impulse = "Inflation", response = "SP500_Ret", 
                        n.ahead = 12, boot = TRUE)

# Plot the IRF
plot(irf_inf_to_vix, main = "Impulse Response: Shock to Inflation -> Response of VIX")
plot(irf_inf_to_sp500, main = "Impulse Response: Shock to Inflation -> Response of S&P 500 Returns")