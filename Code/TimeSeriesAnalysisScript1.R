
#DATA
#libraries
library(quantmod)
library(tseries)
library(forecast)
library(vars)
# Download Data
getSymbols("CPIAUCSL", src = "FRED") # Monthly CPI


getSymbols(c("^VIX", "^GSPC"), src = "yahoo", from = "2009-01-01", to = "2024-01-01") 

# Convert Daily to Monthly using last observation of the month
vix_m <- to.monthly(VIX)$VIX.Close
sp500_m <- to.monthly(GSPC)$GSPC.Close
cpi_m <- CPIAUCSL

# Fix Date Alignment: Convert CPI dates to the 'yearmon' format used by Yahoo data
index(cpi_m) <- as.yearmon(index(cpi_m))

# Calculate Log Returns and Inflation Rate
sp500_ret <- diff(log(sp500_m))
inf_rate <- diff(log(cpi_m))

# Merge into a single dataset aligned by date
ts_data <- merge(sp500_ret, inf_rate, vix_m, all = FALSE)
colnames(ts_data) <- c("SP500_Ret", "Inflation", "VIX")

# Create lagged versions for dynamic modeling
ts_data$Inf_Lag <- quantmod::Lag(ts_data$Inflation, k = 1)
ts_data$VIX_Lag <- quantmod::Lag(ts_data$VIX, k = 1)

# Remove NAs generated by diff() and Lag()
ts_data <- na.omit(ts_data)

# SUBSET THE DATA: Restrict exactly to 2010 through 2023
ts_data <- ts_data["2010/2023"]


# PLOT RAW DATA
par(mfrow = c(3, 1), mar = c(2, 4, 5, 1))

